def calculate_statistics(samples):
    """Calculate statistics for text samples"""
    char_lengths = [len(sample['source']) for sample in samples]
    word_counts = [len(sample['source'].split()) for sample in samples]

    stats = {
        'character_lengths': {
            'min': min(char_lengths),
            'max': max(char_lengths),
            'mean': round(statistics.mean(char_lengths), 2),
            'median': statistics.median(char_lengths),
        },
        'word_counts': {
            'min': min(word_counts),
            'max': max(word_counts),
            'mean': round(statistics.mean(word_counts), 2),
            'median': statistics.median(word_counts),
        }
    }

    return stats

